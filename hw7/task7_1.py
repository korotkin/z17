#сантиметры в дюймы

def cm_to_in(val = 1):
    return val * 2.54

#дюймы в сантиметры

def in_to_cm(val = 1):
    return val * 0.394

#мили в километры

def miles_to_km(val = 1):
    return val * 0.62

#километры в мили

def km_to_miles(val = 1):
    return val * 1.6

#фунты в килограммы

def pounds_to_kg(val = 1):
    return val * 0.45

#килограммы в фунты

def kg_to_pounds(val = 1):
    return val * 2.2

#унции в граммы

def oz_to_gramms(val = 1):
    return val * 28.34

#граммы в унции

def gramms_to_oz(val = 1):
    return val * 0.035

#литры в галлоны

def liters_to_gal(val = 1):
    return val * 0.26

#галлоны в литры

def gal_to_liters(val = 1):
    return val * 3.79

#пинты в литры

def pints_to_liters(val = 1):
    return val * 0.568

#литры в пинты

def liters_to_pints(val = 1):
    return val * 1.76
